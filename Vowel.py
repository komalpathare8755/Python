""" 
Write a python program to check whether entered character is Vowel or not?
 """

Ch=input("Enter a character:")

if Ch=='a' or Ch=='i' or Ch=='e' or Ch=='o' or Ch=='u' or Ch=='A' or Ch=='E' or Ch=='I' or Ch=='O' or Ch=='U' :
    print(Ch," is Vowel.")

else:
    print(Ch,"is not Vowel.")